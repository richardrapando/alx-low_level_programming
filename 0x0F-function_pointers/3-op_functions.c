#include "3-calc.h"

/**
 * op_add - function that adds two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that finds difference of two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that finds product of two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - functions that finds division of two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that finds the modulus of two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: modulus
 */

int op_mod(int a, int b)
{
	return (a % b);
}
